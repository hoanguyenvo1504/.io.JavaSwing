    private void cb_suapanelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cb_suapanelActionPerformed
        listModel.clear();

        khList.clear();
        this.searchDataFromKhachHang();
        tbl_table.setModel(listModel);
        for ( ClassKhachHang db : khList) {
            if(cb_suapanel.getSelectedItem().toString().equals("Tên khách hàng")){
                listModel.addElement(db.getTenkhachhang().toString());
                this.tbl_table.setModel(listModel);
            }
            else if(cb_suapanel.getSelectedItem().toString().equals("Loại khách hàng")){
                listModel.addElement(db.getLoaikhachhang().toString());
                this.tbl_table.setModel(listModel);
            }
            else if(cb_suapanel.getSelectedItem().toString().equals("Địa chỉ")){
                listModel.addElement(db.getDiachi().toString());
                this.tbl_table.setModel(listModel);
            }
            else if(cb_suapanel.getSelectedItem().toString().equals("Số điện thoại")){
                listModel.addElement(db.getSdtkhachhang().toString());
                this.tbl_table.setModel(listModel);
            }
            else if(cb_suapanel.getSelectedItem().toString().equals("Giới Tính")){
                listModel.addElement(db.getGioitinh().toString());
                this.tbl_table.setModel(listModel);
            }
            else{
                listModel.addElement(db.getEmail().toString());
                this.tbl_table.setModel(listModel);
            }
        }
    }//GEN-LAST:event_cb_suapanelActionPerformed


    public void updateDataIntoKhachHang(String sql){
        try {
            Connection connection = new dbconnection().connectionDB();
            if (connection == null) {
                JOptionPane.showMessageDialog(this, "Message", "Error connect to DB", JOptionPane.INFORMATION_MESSAGE);
            }            
            PreparedStatement pst = connection.prepareStatement(sql);
            pst.setString(1, this.txt_sua_hoten.getText());
            pst.setString(2, this.txt_sua_sdt.getText());
            pst.setString(3, this.cb_suapanel_gioitinh.getSelectedItem().toString());
            pst.setString(4, this.txt_sua_diachi.getText());
            pst.setString(5, this.txt_sua_email.getText());
            pst.setString(6, this.cb_suapanel_loaikhachhang.getSelectedItem().toString());
            JOptionPane.showMessageDialog(this, "Updated success", "Message", JOptionPane.INFORMATION_MESSAGE);
            pst.executeUpdate();
            connection.close();
        } catch (Exception e) {
        }
    }
    
    public void searchDataFromKhachHang() {
        try {
            Connection connection = new dbconnection().connectionDB();
            if (connection == null) {
                JOptionPane.showMessageDialog(this, "Message", "Error connect to database", JOptionPane.INFORMATION_MESSAGE);
            }
            Statement st = connection.createStatement();
            
            String sql= "SELECT * FROM khachhang ORDER BY tenkhachhang";
            
            ResultSet rs = st.executeQuery(sql);

            if (rs.isBeforeFirst() == false) {
                JOptionPane.showMessageDialog(this, "Message", "Not Data!", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            while (rs.next()) {
                ClassKhachHang data = new ClassKhachHang();                
                data.setTenkhachhang(rs.getString("tenkhachhang"));
                data.setSdtkhachhang(rs.getString("sdtkhachhang"));
                data.setGioitinh(rs.getString("gioitinh"));
                data.setDiachi(rs.getString("diachi"));
                data.setEmail(rs.getString("email"));
                data.setLoaikhachhang(rs.getString("loaikhachhang"));
                khList.add(data);
            }
            
            connection.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
